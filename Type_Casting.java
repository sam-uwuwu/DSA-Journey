public class Type_Casting {
    public static void main(String[] args) {
        // implicit Type casting 
       char sam = 'B';
    int x = sam ;
    System.out.println(x);
    /// Explicit Type casting
    char Nanndani = 'b';
    int y = (int)Nanndani;
    System.out.println(y);
    char ch = 'z';
    System.out.println(ch + 0);
    int v = 35;
    char Chh =(char)v;
    System.out.println(Chh);
    }
}
